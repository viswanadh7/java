class DataTypes {
    public static void main(String a[]){
        int num = 5;
        byte byteVariable = -128;  // -128 to 127
        short shortVariable = 560; // -32,768 to 32,767
        int intVariable = 45678; // -2^31 to 2^31 -1
        long longVariable = 234567890; // -2^63 to 2^63 -1
        float floatVariable = 1.12f; // put f at end, it asumes as double and takes more memory
        double doubleVariable = 1.123456789;
        char charVariable = 'V'; // stores only single character
        boolean booleanVariable = false;
        System.out.println(num);//println is used to print in new line
    }
}